        private void lsDtCullinarygo() throws SQLException{
            Connection cnn = koneksi();
            
            dtm.getDataVector().removeAllElements();
            dtm.fireTableDataChanged();
            
             if(!cnn.isClosed()){
                 PreparedStatement ps = cnn.prepareStatement("SELECT * FROM cullinarygo;");
                  ResultSet rs = ps.executeQuery();
                  while( rs.next()){
                      Object[] dta = new Object[4];
                      dta[0] = rs.getString("id");
                      dta[1] = rs.getString("Nama");
                      dta[2] = rs.getString("kategory");
                      dta[3] = rs.getString("harga");
                      dta[4] = rs.getString("Tempat");
                      
                dtm.addRow(dta);
                     
                  }
                  cnn.close();
             }
        }  
        private void storeData() throws SQLException{
            Connection cnn = koneksi();
            
            if (!cnn.isClosed()){
               PreparedStatement ps = cnn.prepareStatement("INSERT INTO cullinarygo(id,Nama,kategory,Harga,Tempat) VALUES (?,?,?,?);");
               ps.setString(1, txid.getText());
               ps.setString(2, txnama.getText());
               ps.setString(3, txkategory.getText());
               ps.setString(4, txharga.getText());
               ps.setString(5, txtempat.getText());
               ps.executeUpdate();      
                cnn.close();
            }
                }
        private void updateData()throws SQLException{
             Connection cnn = koneksi();
            if (!cnn.isClosed()){
               PreparedStatement ps = cnn.prepareStatement("UPDATE cullinarygo SET Id=?,Nama=?,Kategory=?,Harga=?,Tempat=?,WHERE Nama=?;");
               ps.setString(4, txid.getText());
               ps.setString(1, txnama.getText());
               ps.setString(2, txkategory.getText());
               ps.setString(3, txharga.getText());
               ps.setString(3, txtempat.getText());
               ps.executeUpdate();      
                cnn.close();
            }
        }
        private void destoryData()throws SQLException{
          Connection cnn = koneksi();
            if (!cnn.isClosed()){
               PreparedStatement ps = cnn.prepareStatement("DELETE FROM cullinarygo WHERE Id=?;");
               ps.setString(1, txid.getText());
               ps.executeUpdate();      
                cnn.close();   
            }
        }

    

