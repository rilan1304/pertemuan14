    private void cUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cUbahActionPerformed
     
        
        if (cUbah.getText().equals("Ubah")){
            cUbah.setText("Simpan");
            cTutup.setText("Batal");
            
            cBaru.setEnabled(false);
            cHapus.setEnabled(false);
            fieldEnabled(true);
            txID.setEditable(false);
        }else{
            
            try{
                updateData();
                lsDtcullinarygo();
                clearform();
                fieldEnabled(false);
            
            cUbah.setText("Ubah");
            cTutup.setText("Tutup");
            cBaru.setEnabled(true);
       } catch (SQLException ex) {
    Logger.getLogger(cullinarygo.class.getName()).log(Level.SEVERE, null, ex);
}
      }     
    }//GEN-LAST:event_cUbahActionPerformed
    private void cHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cHapusActionPerformed
       int jwb = JOptionPane.showOptionDialog(
               this,
               "Yakin akan mengahpus data "+txid.getText()+"?",
               "Konfirmasi Hapus Data",
               JOptionPane.YES_NO_OPTION,
               JOptionPane.QUESTION_MESSAGE,
               null, null, null);
       if(jwb == JOptionPane.YES_OPTION ){
        try {
            destoryData();
            lsDtcullinarygo();
            clearForm();
            cUbah.setEnabled(false);
            cHapus.setEnabled(false);
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(cullinarygo.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
       }
    }//GEN-LAST:event_cHapusActionPerformed

    private void cTutupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cTutupActionPerformed
        if(cTutup.getText().equals("Tutup")){
        int opsi = JOptionPane.showOptionDialog(this, "Yakin akan Menutup Form?", "Konfirmasi Tutup Form", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
        if(opsi == JOptionPane.YES_OPTION){
           System.exit(0);
        }
    }else{
            cTutup.setText("Tutup");
            cBaru.setText("Baru");
            cUbah.setText("Ubah");
               
            fieldEnabled(false);
            
            }
    }//GEN-LAST:event_cTutupActionPerformed

    private void tcullinarygoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tcullinarygoMouseClicked
        txid.setText(tcullinarygo.getValueAt(tcullinarygo.getSelectedRow(), 1).toString() );
        txnama.setText(tcullinarygo.getValueAt(tcullinarygo.getSelectedRow(), 0).toString() );
        String jkx =(tcullinarygo.getValueAt(tcullinarygo.getSelectedRow(), 2).toString().equals("L"))?"Laki-Laki":"Perempuan";
        txkategory.setText(jkx);
        txharga.setText(tcullinarygo.getValueAt(tcullinarygo.getSelectedRow(), 3).toString() );
        cUbah.setEnabled(true);
        cHapus.setEnabled(true);
    }//GEN-LAST:event_tcullinarygoMouseClicked

    private void cBaruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cBaruActionPerformed
       if (cBaru.getText().equals("Baru")){
           cBaru.setText("Simpan");
           cTutup.setText("Batal");
           cUbah.setEnabled(false);
           cHapus.setEnabled(false); 
           clearForm();
           fieldEnabled(true);
       }else{
           if(!txid.getText().equals("")){
               try {   
                   storeData();
                   lsDtcullinarygo();
               } catch (SQLException ex) {
                   java.util.logging.Logger.getLogger(cullinarygo.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
               }
           }
        cBaru.setText("Baru");
        cTutup.setText("Tutup");
         clearForm();
           fieldEnabled(false);
    }//GEN-LAST:event_cBaruActionPerformed
    }
